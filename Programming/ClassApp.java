
public class ClassApp {

	public static void main(String[] args) {

		System.out.println(Math.PI);	// 파이값
		System.out.println(Math.floor(1.6)); // 올림
		System.out.println(Math.ceil(1.6)); // 내림
		System.out.println(String.format("I ate %d apples", 3));
		System.out.println(String.format("%10s", "hi"));
		System.out.println(String.format("%-10sjane.", "hi"));
	}

}
